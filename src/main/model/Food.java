package main.model;

public class Food {

    public Food() {}

    public Food(String id, String foodType, String name, double price) {
        this.id = id;
        this.foodType = foodType;
        this.name = name;
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFoodType() {
        return foodType;
    }

    public void setFoodType(String foodType) {
        this.foodType = foodType;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    private String id;
    private String foodType;
    private String name;
    private double price;
}
